import mongoose from 'mongoose';

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
  role: { type: String, default: "admin" }, // keep role explicit

}, { timestamps: true });





const adminModel = mongoose.model('User', userSchema);

export default adminModel